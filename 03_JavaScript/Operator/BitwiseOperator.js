let a = 4;
let b = 5;

console.log(a | b); //Bitwise OR    // Output:-  5

console.log(a & b); //Bitwise AND  // Output:-  4

console.log(a ^ b);  // Bitwise XOR  // Output:- 1

console.log(4 << 5); //Left Shift   // Output:- 128

// or 

console.log(1 << 3);  //Left Shift   // Output:- 8

console.log(27 >> 2); //Right Shift   // Output:- 6